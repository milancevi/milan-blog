@app.route('/register', methods=["GET", "POST"])
def register():
    new_form = RegisterForm()
    if new_form.validate_on_submit():
        new_user = User(email=new_form.email.data,
                        password=generate_password_hash(new_form.password.data),
                        name=new_form.name.data)
        user_exists = db.session.query(User).filter(User.email == new_user.email).first()
        if user_exists is not None:
            flash("You've already signed up with that email, use Login instead!")
            return redirect(url_for('login'))
        db.session.add(new_user)
        db.session.commit()
        login_user(new_user)
        return redirect(url_for('get_all_posts'))
    return render_template("register.html", form=new_form)


@app.route('/login', methods=["GET", "POST"])
def login():
    new_login = LoginForm()
    error = None
    if new_login.validate_on_submit():
        user = db.session.query(User).filter(User.email == new_login.email.data).first()
        if user is not None:
            if check_password_hash(user.password, new_login.password.data):
                login_user(user)
                flash(f'Welcome, {str(user.name).title()}!')
                return redirect(url_for('get_all_posts'))
            else:
                error = "Password incorrect, please try again."
        else:
            error = "Sorry, that email does not exist, please try again."
    return render_template("login.html", form=new_login, error=error)


@app.route('/logout')
def logout():
    if current_user.is_authenticated:
        logout_user()
        flash(f"You've logged out successfully.")
    else:
        flash("You are not logged in.")
    return redirect(url_for('get_all_posts'))


@app.route("/post/<int:post_id>", methods=["GET", "POST"])
def show_post(post_id):
    requested_post = BlogPost.query.get(post_id)
    comment_form = CommentForm()
    if comment_form.validate_on_submit():
        if not current_user.is_authenticated:
            flash("You need to login or register to comment.")
            return redirect(url_for('login'))
        new_comment = Comment(text=comment_form.comment_text.data,
                              author_id=current_user.id)
        db.session.add(new_comment)
        db.session.commit()
    return render_template("post.html", post=requested_post, form=comment_form)


