def admin_only(f):
    @wraps(f)
    def decorated_function(*args, **kwargs):
        # if id is not 1 then return 403 with abort error
        if current_user.id != 1:
            return abort(403)
        # else continue with the route function
        return f(*args, **kwargs)
    return decorated_function


